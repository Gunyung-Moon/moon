'''
BMI (Body Mass Index)
1.건강을 위해 비만도를 체크하여 봅시다. 
신장과 체중을 입력하고 당신의 비만도 (BMI = 체중 (kg) ÷ 신장 (m)의 제곱)을 계산하는 프로그램을 작성하고 계산 결과를 확인하십시오.
판정 기준 
	18.5 미만 → 마른, 
	18.5 ~ 25 미만 → 표준 
	25-30 미만 → 비만 
	30 이상 → 고도 비만
으로 판정합니다.

키를 cm 단위로 입력하십시오> 170
체중을 kg 단위로 입력하십시오> 65
BMI = 22.49134948096886
당신은 "표준"입니다.
'''

# 집에서 type 아니면 처음과정으로 돌아가겠음 코딩할 껄

Height = int(input('키를 cm 단위로 입력하십시오. '))
Weight = int(input('체중을 kg 단위로 입력하십시오. '))
BMI = Weight / ((Height / 100) ** 2)
print('당신의 BMI는 ', BMI)
if BMI < 18.5 :
    print('당신은 "멸공"입니다.')
elif 18.5 <= BMI < 25 :
    print('당신은 "멸치"입니다.')
elif 25 <= BMI < 30 :
    print('당신은 "돼지"입니다.')
else :
    print('당신은 "돼공"입니다.')

'''
윤년의 판정
2.윤년이라는 것은 4 년에 한 번, 2월 29일까지 있는 연도입니다. 
첫번째 조건으로 연도를 400으로 나눈 나머지가 0일 경우, 윤년입니다.
두번째 조건으로 연도를 100으로 나눈 나머지가 0이면, 윤년이 아닙니다.        예) 123
세번째 조건으로 연도를 4로 나눈 나머지가 0이면 윤년입니다.

이 규칙에 따라 서기 연도를 입력하고 윤년을 확인할 수 있는 프로그램을 작성합니다.
서기 연도를 입력하십시오> 1996
1996 년은 윤년입니다.
'''

year = int(input('서기 연도를 입력하십시오 '))
if (year % 400) == 0 :
    print('year는 윤년입니다 ')
elif (year % 100) == 0 :
    print('year는 윤년이 아닙니다.')
elif(year % 4) == 0 :
    print('year는 윤년입니다.')
else :
    print('year는 윤년이 아닙니다.')
